package ex_01_Java_Basics;

public class Lab006_Final_Example {
    public static void main(String[] args) {
        // we can create constant variable by using final keyword
        // it can not be change through out execution
        final float PI=3.14F;
        //PI is final in nature
        //System.out.println(PI);
        System.out.printf("PI value is = %f", PI);
    }
}
