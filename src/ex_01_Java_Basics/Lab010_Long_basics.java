package ex_01_Java_Basics;

public class Lab010_Long_basics {
    public static void main(String[] args) {

        System.out.println("1. What is the size of a long in Java?");
        System.out.println("Answer: 64 bits (8 bytes).");
        System.out.println("2. What is the range of a long in Java?");
        System.out.println("Answer: -2^63 to 2^63 - 1 (i.e., from -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807).");
        System.out.println("3. Is long signed or unsigned in Java?");
        System.out.println("Answer: long is signed in Java.");
        System.out.println("4. Can a long hold decimal numbers?");
        System.out.println("Answer: No. long holds only whole numbers (integers). Use float or double for decimals.");

        System.out.println("5. What is the default value of a long variable?");
        System.out.println("Answer: 0L");
    }
}
