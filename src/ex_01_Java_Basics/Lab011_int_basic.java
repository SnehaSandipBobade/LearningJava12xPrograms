package ex_01_Java_Basics;

public class Lab011_int_basic {
    public static void main(String[] args) {
        System.out.println("1. What is the size of int in Java?");
        System.out.println("Answer: 32 bits (4 bytes)");
        System.out.println("2. What is the range of int in Java?");
        System.out.println("Answer: From -2,147,483,648 to 2,147,483,647 (i.e., -2³¹ to 2³¹ - 1)");
        System.out.println("3. Is int a signed or unsigned type in Java?");
        System.out.println("Answer: Signed");
        System.out.println("4. What is the default value of an int in Java?");
        System.out.println("Answer: 0");
        System.out.println("5. What happens if an int value exceeds its range?");
        System.out.println("Answer: It wraps around due to overflow (no error is thrown)");
    }
}
