package ex_10_For_Loop;

public class Lab108_ForLoop_decr2 {
    public static void main(String[] args) {
        for (int i =10;i>0;i--)
        {
            System.out.println(i);

        }
        System.out.println("--");
        for (int i =10;i>=0;i--)
        {
            System.out.println(i);

        }
        System.out.println("--");

        for (int i =9;i>=1;i--)
        {
            System.out.println(i);

        }
        System.out.println("--");

        for (int i =9;i>1;i--)
        {
            System.out.println(i);

        }
    }
}
